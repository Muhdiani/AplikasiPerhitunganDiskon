    private double getComboBoxDiskon() {
        String selectedItem = (String) jComboBoxDiskon.getSelectedItem();
        if (selectedItem != null && selectedItem.endsWith("%")) {
            try {
                // Parse string "XX%" menjadi nilai desimal (e.g., 0.10)
                double diskon = Double.parseDouble(selectedItem.replace("%", "")) / 100.0;
                return diskon;
            } catch (NumberFormatException e) {
                return 0.0;
            }
        }
        return 0.0;
    }
    

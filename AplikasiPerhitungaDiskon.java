    private void jButtonHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHitungActionPerformed
        // Logika perhitungan diskon
        try {
            // 1. Ambil Harga Awal
            String hargaText = jTextFieldInputHarga.getText();
            double hargaAwal = Double.parseDouble(hargaText);
            
            // 2. Ambil Diskon Dasar (sudah diatur oleh JComboBox atau JSlider)
            double diskonDasar = currentDiskonPercentage;
            
            // 3. Ambil Diskon Kupon Tambahan
            double diskonKuponTambahan = 0.0;
            String kodeKupon = jTextFieldKodeKupon.getText().toUpperCase();
            if (kuponDiskon.containsKey(kodeKupon)) {
                diskonKuponTambahan = kuponDiskon.get(kodeKupon);
            }
            
            // 4. Hitung Total Diskon
            // Diskon total = 1 - ((1 - Diskon Dasar) * (1 - Diskon Kupon Tambahan))
            // Ini adalah cara umum menghitung diskon berjenjang.
            double totalDiskonRate = 1.0 - ((1.0 - diskonDasar) * (1.0 - diskonKuponTambahan));
            
            // 5. Hitung Harga Akhir dan Penghematan
            double penghematan = hargaAwal * totalDiskonRate;
            double hargaAkhir = hargaAwal - penghematan;
            
            // 6. Tampilkan Hasil
            jTextFieldHargaAkhir.setText(String.format("Rp %,.2f", hargaAkhir));
            jTextFieldPenghematan.setText(String.format("Rp %,.2f", penghematan));
            
            // 7. Tambahkan ke Riwayat
            String riwayatEntry = String.format(
                "Harga: Rp %,.2f | Diskon: %.0f%% | Kupon: %.0f%% | Akhir: Rp %,.2f\n",
                hargaAwal, 
                diskonDasar * 100, 
                diskonKuponTambahan * 100,
                hargaAkhir
            );
            jTextAreaRiwayat.append(riwayatEntry);

        } catch (NumberFormatException e) {
            // Penanganan Eksepsi (jika input harga bukan angka)
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Mohon masukkan angka yang valid untuk Harga.", 
                "Kesalahan Input", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
            jTextFieldInputHarga.requestFocus();
        }
    }//GEN-LAST:event_jButtonHitungActionPerformed

